import pprint

class Solution:
    def removeCoveredIntervals(self, intervals):
        si = sorted(intervals, key=lambda x: x[1]-x[0], reverse=True)
        # pprint.pprint(si)
        cur = 0
        while True:
            if cur == len(si):
                break
            to_del = []
            for i in range(cur+1, len(si)):
                if si[cur][0] <= si[i][0] and si[cur][1] >= si[i][1]:
                    to_del.insert(0, i)
            # print(cur, to_del)
            for i in to_del:
                del si[i]
            cur += 1
        # pprint.pprint(si)
        # print(len(si))
        return len(si)

